let currentCat = "smileys";
let emojiBtn, emojiPicker, emojiGrid, emojiSearch, messageInput, sendBtn;

function initEmojiPicker() {
    emojiBtn = document.getElementById("emojiBtn");
    emojiPicker = document.getElementById("emojiPicker");
    emojiGrid = document.getElementById("emojiGrid");
    emojiSearch = document.getElementById("emojiSearch");
    messageInput = document.getElementById("messageInput");
    sendBtn = document.getElementById("sendBtn");

    if (!emojiBtn) return;

    renderEmojis(emojiData.smileys);

    emojiBtn.addEventListener("click", (e) => {
        e.stopPropagation();
        emojiPicker.classList.toggle("show");
    });

    document.addEventListener("click", (e) => {
        if (!emojiPicker.contains(e.target) && e.target !== emojiBtn) {
            emojiPicker.classList.remove("show");
        }
    });

    document.querySelectorAll(".emoji-cat-btn").forEach((btn) => {
        btn.addEventListener("click", () => {
            document
                .querySelectorAll(".emoji-cat-btn")
                .forEach((b) => b.classList.remove("active"));
            btn.classList.add("active");
            currentCat = btn.dataset.cat;
            renderEmojis(emojiData[currentCat]);
        });
    });

    emojiSearch.addEventListener("input", (e) => {
        if (!e.target.value) {
            renderEmojis(emojiData[currentCat]);
            return;
        }
        const searchTerm = e.target.value.toLowerCase();
        const allEmojis = Object.values(emojiData).flat();
        const filtered = allEmojis.filter((emoji) =>
            emoji.toLowerCase().includes(searchTerm),
        );
        renderEmojis(filtered);
    });

    emojiGrid.addEventListener("click", (e) => {
        if (e.target.classList.contains("emoji-item")) {
            insertEmoji(e.target.textContent);
        }
    });

    messageInput.addEventListener("input", () => {
        adjustTextareaHeight(messageInput);
        if (typeof window.handleLocalTypingInput === "function") {
            window.handleLocalTypingInput(messageInput.value);
        }
    });

    messageInput.addEventListener("keydown", (e) => {
        if (e.key === "Enter" && !e.shiftKey) {
            e.preventDefault();
            if (typeof window.sendMessage === "function") {
                window.sendMessage();
            }
            if (typeof window.handleLocalTypingInput === "function") {
                window.handleLocalTypingInput("");
            }
            messageInput.style.height = "34px";
        }
    });

    sendBtn?.addEventListener("click", () => {
        if (typeof window.sendMessage === "function") {
            window.sendMessage();
        }
        if (typeof window.handleLocalTypingInput === "function") {
            window.handleLocalTypingInput("");
        }
        messageInput.style.height = "34px";
    });
}

function renderEmojis(emojis) {
    emojiGrid.innerHTML = emojis
        .map((e) => `<span class="emoji-item">${e}</span>`)
        .join("");
}

function insertEmoji(emoji) {
    const start = messageInput.selectionStart;
    const end = messageInput.selectionEnd;
    const text = messageInput.value;
    messageInput.value = text.substring(0, start) + emoji + text.substring(end);
    messageInput.focus();
    messageInput.selectionStart = messageInput.selectionEnd =
        start + emoji.length;
    adjustTextareaHeight(messageInput);
}

document.addEventListener("DOMContentLoaded", initEmojiPicker);
